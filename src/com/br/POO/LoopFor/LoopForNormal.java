package com.br.POO.LoopFor;

public class LoopForNormal {
    public static void main(String[] args){

        System.out.println("Incrementando o valor");
        for (int i = 0; i < 5; i++){
            System.out.println("i tem valor: " + i);
        }

        System.out.println("\nDecrementando o valor");
        for (int i = 5; i >= 0; i--){
            System.out.println("i tem valor: " + i);
        }
    }
}
