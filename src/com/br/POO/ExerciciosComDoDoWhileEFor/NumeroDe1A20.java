package com.br.POO.ExerciciosComDoDoWhileEFor;

public class NumeroDe1A20 {
    public static void main(String[] args){
        int numero;

        for (numero = 1; numero <= 20; numero++){
            System.out.print(numero + " ");
        }
        System.out.println();
        for (numero = 1; numero <= 20; numero++){
            System.out.println(numero);
        }
    }
}
